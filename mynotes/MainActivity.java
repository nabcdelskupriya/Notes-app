public class MainActivity extends AppCompatActivity {


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        FirebaseUser user = FirebaseAuth.getInstance().getCurrentUser();
        String uid = user.getUid();
    BottomNavigationView bottomNavigationView = findViewById(R.id.bottm_navigation);
        bottomNavigationView.setOnNavigationItemSelectedListener(onNav);

    getSupportFragmentManager().beginTransaction().replace(R.id.frame_layout,
                new Fragment1()).commit();

        FirebaseMessaging.getInstance().getToken()
                .addOnCompleteListener(new OnCompleteListener<String>() {
                    @Override
                    public void onComplete(@NonNull Task<String> task) {
                        if (!task.isSuccessful()) {
                            //    Log.w(TAG, "Fetching FCM registration token failed", task.getException());
                            Toast.makeText(MainActivity.this, "Token is missing", Toast.LENGTH_SHORT).show();
                            return;
                        }

                        // Get new FCM registration token
                        String token = task.getResult();
                        FirebaseDatabase.getInstance().getReference("Token").child(uid).child("token").setValue(token);

                    }
                });
    }
    private BottomNavigationView.OnNavigationItemSelectedListener onNav =
            new BottomNavigationView.OnNavigationItemSelectedListener() {
                @Override
                public boolean onNavigationItemSelected(@NonNull MenuItem item) {
                    Fragment selected = null;
                    switch ((item.getItemId())){
                        case R.id.ask:
                            selected = new Fragment1();
                            break;
                        case R.id.ask2:
                            selected = new Fragment2();
                            break;
                        case R.id.ask3:
                            selected = new Fragment3();
                            break;
                    }


                    getSupportFragmentManager().beginTransaction().replace(R.id.frame_layout,
                            selected).commit();


                    return true;
                }

            };
}


